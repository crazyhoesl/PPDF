window.PPDF_DETECT_LANG = function () {
  const stored = localStorage.getItem('ppdf-lang');
  if (stored && window.PPDF_I18N[stored]) return stored;
  const navLang = (navigator.language || navigator.userLanguage || 'fr').slice(0, 2).toLowerCase();
  if (window.PPDF_I18N[navLang]) return navLang;
  return window.PPDF_DEFAULT_LANG;
};
